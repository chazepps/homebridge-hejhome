export function absolutePath(relativePath) {
  return path.join(ROOT, relativePath);
}

export function exists(relativePath) {
  return fs.existsSync(absolutePath(relativePath));
}

export function readText(relativePath) {
  return fs.readFileSync(absolutePath(relativePath), 'utf8');
}

export function writeIfChanged(relativePath, content) {
  const target = absolutePath(relativePath);
  fs.mkdirSync(path.dirname(target), { recursive: true });
  if (fs.existsSync(target) && fs.readFileSync(target, 'utf8') === content) {
    return false;
  }
  fs.writeFileSync(target, content);
  return true;
}

export function listMarkdownFiles() {
  return listFiles(ROOT)
    .filter((filePath) => filePath.endsWith('.md'))
    .map((filePath) => path.relative(ROOT, filePath))
    .sort();
}

export function listProjectFiles() {
  return listFiles(ROOT)
    .map((filePath) => path.relative(ROOT, filePath))
    .filter((relativePath) => relativePath !== '.DS_Store')
    .filter((relativePath) => !IGNORED_PATH_PREFIXES.some((prefix) => relativePath.startsWith(prefix)))
    .sort();
}

export function validateMarkdownLinks(relativePath) {
  const errors = [];
  const text = readText(relativePath);
  const linkPattern = /!?\[[^\]]*]\(([^)]+)\)/g;
  let match;
  while ((match = linkPattern.exec(text))) {
    const target = normalizeLinkTarget(match[1]);
    if (!target || shouldSkipLink(target)) {
      continue;
    }

    const resolved = target.startsWith('/')
      ? target
      : path.resolve(path.dirname(absolutePath(relativePath)), target);

    if (!fs.existsSync(resolved)) {
      errors.push(`broken markdown link in ${relativePath}: ${match[1]}`);
    }
  }
  return errors;
}

export function validateForbiddenContent(relativePath) {
  const text = readText(relativePath);
  const errors = [];
  for (const rule of FORBIDDEN_DOC_PATTERNS) {
    if (rule.pattern.test(text)) {
      errors.push(`forbidden ${rule.name} in ${relativePath}`);
    }
  }
  return errors;
}

export function renderTree(relativePaths) {
  const root = {};
  for (const relativePath of relativePaths) {
    const parts = relativePath.split(path.sep);
    let cursor = root;
    for (const part of parts) {
      cursor[part] ??= {};
      cursor = cursor[part];
    }
  }

  const lines = ['.'];
  appendTreeLines(lines, root, '');
  return `${lines.join('\n')}\n`;
}

function listFiles(directory) {
  const entries = fs.readdirSync(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    if (entry.name === '.DS_Store') {
      continue;
    }
    const fullPath = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      if (IGNORED_DIRS.has(entry.name)) {
        continue;
      }
      files.push(...listFiles(fullPath));
    } else if (entry.isFile()) {
      files.push(fullPath);
    }
  }

  return files;
}

function appendTreeLines(lines, node, prefix) {
  const names = Object.keys(node).sort((a, b) => a.localeCompare(b));
  names.forEach((name, index) => {
    const last = index === names.length - 1;
    const child = node[name];
    const branch = last ? '└── ' : '├── ';
    lines.push(`${prefix}${branch}${name}`);
    if (Object.keys(child).length > 0) {
      appendTreeLines(lines, child, `${prefix}${last ? '    ' : '│   '}`);
    }
  });
}

function normalizeLinkTarget(rawTarget) {
  let target = rawTarget.trim();
  if (target.startsWith('<') && target.endsWith('>')) {
    target = target.slice(1, -1);
  }
  return target.split('#')[0].split('?')[0].trim();
}

function shouldSkipLink(target) {
  return (
    target.length === 0 ||
    target.startsWith('#') ||
    /^[a-zA-Z][a-zA-Z0-9+.-]*:/.test(target)
  );
}
